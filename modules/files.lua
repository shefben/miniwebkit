
table.filter = function(t, filterIter)
    local out = {}
    for _, v in ipairs(t) do
        if filterIter(v) then
            table.insert(out, v)
        end
    end
    return out
end

table.map = function(t, iter)
    local out = {}
    for k, v in ipairs(t) do
        table.insert(out, iter(v, k))
    end
    return out
end

